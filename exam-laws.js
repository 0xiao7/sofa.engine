function examFilterLaws(laws) {
  try {
    var ef = localStorage.getItem('sofa_exam_filter') || 'all';
    if (ef === 'cpa' || ef === 'land') {
      var s = new Set(EXAM_LAW_SETS[ef]);
      return laws.filter(function(l){ return s.has(l.name); });
    }
  } catch(e) {}
  return laws;
}

function examFilterLabel() {
  try {
    var ef = localStorage.getItem('sofa_exam_filter') || 'all';
    if (ef === 'cpa')  return '記帳士';
    if (ef === 'land') return '地政士';
  } catch(e) {}
  return '';
}
